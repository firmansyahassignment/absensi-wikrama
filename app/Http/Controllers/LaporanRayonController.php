    {
        $rayons = Rayon::where('pembimbing_id', auth()->user()->id)->get();
        $rayon = $rayons->find($rayon);
        if($rayon){
            $absens = Absen::all();
            return view('rayon.laporan_rayon', compact('rayon', 'absens'));
        }

        return abort(404);
    }

